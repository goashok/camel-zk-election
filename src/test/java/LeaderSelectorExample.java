import org.apache.curator.utils.CloseableUtils;
import org.apache.curator.framework.CuratorFramework;
import org.apache.curator.framework.CuratorFrameworkFactory;
import org.apache.curator.retry.ExponentialBackoffRetry;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.util.UUID;

public class LeaderSelectorExample {
    private static final int CLIENT_QTY = 10;

    private static final String PATH = "/examples/leader";

    public static void main(String[] args) throws Exception {
        // all of the useful sample code is in LeaderElector.java
        LeaderElector example = null;
        CuratorFramework client = null;
        String id = UUID.randomUUID().toString();
        System.out.println("Create " + CLIENT_QTY + " clients, have each negotiate for leadership and then wait a random number of seconds before letting another leader election occur.");
        System.out.println("Notice that leader election is fair: all clients will become leader and will do so the same number of times.");

        try {
            client = CuratorFrameworkFactory.newClient("localhost:2181", 60000, 60000, new ExponentialBackoffRetry(1000, 3));
            example = new LeaderElector(client, PATH, new DefaultHandler("Client #" + id));

            client.start();
            example.start();

            System.out.println("Press enter/return to quit\n");
            new BufferedReader(new InputStreamReader(System.in)).readLine();
        } finally {
            System.out.println("Shutting down...");

            CloseableUtils.closeQuietly(example);
            CloseableUtils.closeQuietly(client);

        }
    }
}
